export function getReference(key: string): Reference | undefined {
  return REFERENCES[key];
}

export function listReferences(filter?: {
  category?: ReferenceCategory;
  tag?: string;
  yearAfter?: number;
}): Reference[] {
  return refs.filter((r) => {
    if (filter?.category && r.category !== filter.category) return false;
    if (filter?.tag && !r.tags.includes(filter.tag)) return false;
    if (filter?.yearAfter && r.year < filter.yearAfter) return false;
    return true;
  });
}
